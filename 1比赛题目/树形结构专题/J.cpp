#include<iostream>
#include<cstdio>
#include<cstring>
#include<algorithm>
#include<cmath>
#define maxn 400005
#define maxk 500005
using namespace std;
inline int qread(){
	int x=0,sign=1;
	char c=getchar();
	while(c<'0'||c>'9'){
		if(c=='-') sign=-1;
		c=getchar();
	}
	while(c>='0'&&c<='9'){
		x=x*10+c-'0';
		c=getchar();
	}
	return x*sign;
}
int n,m,k;
int tree_cnt=0;
struct edge{
	int from;
	int to;
	int next;
}E[maxk<<1];
int head[maxn];
int size=0;
void add_edge(int u,int v){
	size++;
	E[size].from=u;
	E[size].to=v;
	E[size].next=head[u];
	head[u]=size;
}

struct react{
	int a;
	int b;
	int deep;
	int _rank;
	react(){
		
	}
	react(int c,int d,int dep,int i){
		a=c;
		b=d;
		deep=dep;
		_rank=i;
	}
}r[maxk];
int cmp(react a,react b){
	return a.deep==b.deep?a._rank<b._rank:a.deep>b.deep;
}
int mass[maxn];
int pos[maxn];
int deep[maxn],anc[maxn][32];
int belong[maxn];
int log2n;
void dfs(int x,int fa){
	belong[x]=tree_cnt;
	deep[x]=deep[fa]+1;
	anc[x][0]=fa;
	for(int i=1;(1<<i)<=deep[x];i++){
		anc[x][i]=anc[anc[x][i-1]][i-1];
	}
	for(int i=head[x];i;i=E[i].next){
		int y=E[i].to;
		if(y!=fa){
			dfs(y,x);
		}
	}
}
int lca(int x,int y){
	if(deep[x]<deep[y]) swap(x,y);
	for(int i=20;i>=0;i--){
		if(deep[anc[x][i]]>=deep[y]){
			x=anc[x][i];
		}
	}
	if(x==y) return x;
	for(int i=20;i>=0;i--){
		if(anc[x][i]!=anc[y][i]){
			x=anc[x][i];
			y=anc[y][i];
		}
	}
	return anc[x][0];
}

int main(){
//	freopen("J.in","r",stdin);
	int u,v;
//	scanf("%d %d %d",&n,&m,&k);
	n=qread();
	m=qread();
	k=qread();
	int tot=n;
	log2n=log2(n)+1;
	for(int i=1;i<=n;i++){
//		scanf("%d",&mass[i]);
		mass[i]=qread();
	}
	for(int i=1;i<=n;i++) pos[i]=i;
	for(int i=1;i<=m;i++){
		tot++;
//		scanf("%d %d",&u,&v);
		u=qread();
		v=qread();
		add_edge(tot,pos[u]);
		add_edge(tot,pos[v]);
		pos[v]=tot;
	}
	for(int i=n+m;i>0;i--){//倒序循环会更快，因为下标越大的点深度越浅，一次可以遍历更多的点，减少dfs次数 
		if(!anc[i][0]){
			tree_cnt++;
			deep[i]=1;
			dfs(i,0);
		}
	}
	int k2=0;
	int c,d;
	for(int i=1;i<=k;i++){
		c=qread();
		d=qread();
		if(belong[c]!=belong[d]) continue;
		k2++;
		r[k2]=react(c,d,deep[lca(c,d)],i);
	}
	sort(r+1,r+1+k2,cmp);
	long long ans=0;
	for(int i=1;i<=k2;i++){
		if(!r[i].deep) continue;
		int down=min(mass[r[i].a],mass[r[i].b]);
		mass[r[i].a]-=down;
		mass[r[i].b]-=down;
		ans+=down*2;
	}
	cout<<ans<<endl;
}
